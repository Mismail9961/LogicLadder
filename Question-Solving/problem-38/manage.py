my_list = [10, 20, 30, 40]
my_tuple = tuple(my_list)

print("Original list:", my_list)     # [10, 20, 30, 40]
print("Converted to tuple:", my_tuple)  # (10, 20, 30, 40)


my_tuple = (10, 20, 30, 40)
my_list = list(my_tuple)

print("Original tuple:", my_tuple)   # (10, 20, 30, 40)
print("Converted to list:", my_list) # [10, 20, 30, 40]
