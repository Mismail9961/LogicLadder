my_tuple = (10, 20, 30, 40, 50)
print("Tuple:", my_tuple)


print("First element:", my_tuple[0])   # Output: 10
print("Third element:", my_tuple[2])   # Output: 30
print("Last element:", my_tuple[-1])   # Output: 50


print("First three elements:", my_tuple[:3])  # Output: (10, 20, 30)
print("From index 2 to end:", my_tuple[2:])   # Output: (30, 40, 50)
